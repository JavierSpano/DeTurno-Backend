package com.javierfspano.deturno.entities;

public class Farmacias {
	
	private String barrio;
	private String calle_altura;
	private String calle_nombre;
	private String codigo_postal;
	private String lat;
	private String lng;
	private String nombre;
	private String telefono;
	public String getBarrio() {
		return barrio;
	}
	public void setBarrio(String barrio) {
		this.barrio = barrio;
	}
	public String getCalle_altura() {
		return calle_altura;
	}
	public void setCalle_altura(String calle_altura) {
		this.calle_altura = calle_altura;
	}
	public String getCalle_nombre() {
		return calle_nombre;
	}
	public void setCalle_nombre(String calle_nombre) {
		this.calle_nombre = calle_nombre;
	}
	public String getCodigo_postal() {
		return codigo_postal;
	}
	public void setCodigo_postal(String codigo_postal) {
		this.codigo_postal = codigo_postal;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getLng() {
		return lng;
	}
	public void setLng(String lng) {
		this.lng = lng;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}

	
	
	

}
